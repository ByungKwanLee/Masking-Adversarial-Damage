def str2bool(v):
    if v.lower() in ('yes', 'true', 't', 'y', '1'):
        return True
    elif v.lower() in ('no', 'false', 'f', 'n', '0'):
        return False
    else:
        assert False

def list_to_concat(adv_delta_L_avg, device):
    size = [x.shape for x in adv_delta_L_avg]
    concat_conv = torch.cat([x.flatten() for x in adv_delta_L_avg[:-1]]).to(device)
    linear = adv_delta_L_avg[-1]  # feature * n_classes
    return concat_conv, linear, size

