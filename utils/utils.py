def index2onehot(index, size):

    if index.dim() == 1:
        try:
            onehot = torch.zeros(size.numel())
        except:
            onehot = torch.zeros(size)
        onehot[index] = 1
    elif index.dim() == 2:
        onehot = torch.zeros(size)
        for i in range(onehot.shape[0]):
            onehot[i, index[i]] = 1
    return onehot.view(size)

def onehot2index(onehot):
    index = []
    for i in range(onehot.shape[0]):
        if onehot[i] != 0:
            index.append(i)
    return torch.tensor(index)

